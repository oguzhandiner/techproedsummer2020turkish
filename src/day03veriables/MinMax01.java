package day03veriables;

public class MinMax01 {
	
	public static void main(String[] args) {
		
		// Primitive Data Type'larin minimum ve maximum degerlerini bulmak.
		
		// 1) int min ve max degerini bulmak.
		
		int minValue = Integer.MIN_VALUE;
		
		System.out.println("int Min.Value: " + minValue);
		
		int maxValue = Integer.MAX_VALUE;
		
		System.out.println("int Max.Value: " + maxValue);
		
		// byte min ve max deger bulma.
		
		byte min = Byte.MIN_VALUE;
		
		System.out.println("byte Min.Value: " + min);
		
		byte max = Byte.MAX_VALUE;
		
		System.out.println("byte Max.Value: " + max);
		
		float minf = Float.MIN_VALUE;
		
		System.out.println("float min.Value: " + minf);
		
	}

}
